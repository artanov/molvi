def rgba(hex_color, alpha=255):
    """'#RRGGBB' + альфа → кортеж (r, g, b, a) для PIL."""
    h = hex_color.lstrip("#")
    return (int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16), alpha)
